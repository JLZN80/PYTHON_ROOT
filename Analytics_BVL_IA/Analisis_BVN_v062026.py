def preparar_data(data, ticker):
    print(f"\n--- Preparing data for {ticker} ---")

    if data.empty:
        print(f"No data downloaded for {ticker}.")
        return pd.DataFrame()

    if isinstance(data.columns, pd.MultiIndex):
        print(f"Flattening MultiIndex columns for {ticker}.")
        data.columns = data.columns.get_level_values(0)

    columnas_necesarias = ["Open", "High", "Low", "Close", "Volume"]

    for col in columnas_necesarias:
        if col not in data.columns:
            print(f"Falta la columna {col} para {ticker}.")
            return pd.DataFrame()

    data = data[columnas_necesarias].copy()

    for col in columnas_necesarias:
        data[col] = pd.to_numeric(data[col], errors="coerce")

    data = data.dropna()
    data = data[["Open", "High", "Low", "Close", "Volume"]]

    print(f"DataFrame for {ticker} prepared. Shape: {data.shape}")
    print(data.head())

    return data

