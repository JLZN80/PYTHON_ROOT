def descargar_close(ticker):
    data = yf.download(
        ticker,
        start=start_date,
        end=end_date,
        progress=False,
        auto_adjust=True
    )

    if data.empty:
        print(f"No se pudo descargar data para {ticker}.")
        return pd.Series(dtype=float)

    if isinstance(data.columns, pd.MultiIndex):
        data.columns = data.columns.get_level_values(0)

    close = pd.to_numeric(data["Close"], errors="coerce")
    close = close.dropna()

    return close


