def calcular_rsi(precios, ventana=14):
    delta = precios.diff()

    ganancias = delta.where(delta > 0, 0)
    perdidas = -delta.where(delta < 0, 0)

    promedio_ganancias = ganancias.rolling(window=ventana).mean()
    promedio_perdidas = perdidas.rolling(window=ventana).mean()

    rs = promedio_ganancias / promedio_perdidas
    rsi = 100 - (100 / (1 + rs))

    return rsi


def calcular_drawdown(precios):
    maximo_acumulado = precios.cummax()
    drawdown = precios / maximo_acumulado - 1
    return drawdown


